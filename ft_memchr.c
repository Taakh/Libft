#include <string.h>

void					*ft_memchr(const void *s, int c, size_t n)
{
	unsigned int		i;
	const unsigned char	*ptr;

	ptr = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (ptr[i] == (unsigned char)c)
			return ((unsigned char *)(ptr + i));
		else
			i++;
	}
	return (NULL);
}
